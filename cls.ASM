public clean ;清屏子程序
.8086
.MODEL small
.stack 10h
.code
;*****************************************************************
; 子程序：clean
; 功能：负责实现清屏功能
; 入口参数：无
; 出口参数：无
;*****************************************************************
clean proc

start: 
	  mov ah,0fh         ;读取显示器模式,dosbox中显示模式是80x25     
	  int 10h
	  mov ah,0h          ;设置显示器模式
	  int 10h

   	  retf

clean endp

end start